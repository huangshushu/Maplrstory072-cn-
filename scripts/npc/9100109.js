function action(mode, type, selection) {
    if (mode == 1) {
        status++;
    } else {
        if (status == 0) {
            cm.sendOk("不想使用了吗？");
            cm.dispose();
        }
        status--;
    }

    if (status == 0) {
        if (cm.haveItem(4110010)) {
            cm.sendYesNo("你背包里有#b#t4110010##k，你要试试手气吗?");
        } else {
            cm.sendOk("你背包里有#b#t4110010##k吗?");
            cm.safeDispose();
        }
    } else if (status == 1) {
        var chance = Math.floor(Math.random() * 1000);
        var finalitem = Array();
        for (var i = 0; i < itemList.length; i++) {
            if (itemList[i][1] >= chance) {
                finalitem.push(itemList[i]);
            }
        }
        if (finalitem.length != 0) {
            var random = new java.util.Random();
            var finalchance = random.nextInt(finalitem.length);
            var itemId = finalitem[finalchance][0];
            var quantity = finalitem[finalchance][2];
            var notice = finalitem[finalchance][3];
            if (cm.haveItem(4110010) && cm.canHold()) {
                cm.gainItem(4110010, -1);
                if (notice == 1) {
                    cm.gainGachaponItem(itemId, quantity, "豆豆票");
                } else {
                    cm.gainItem(itemId, quantity);
                }
                cm.sendOk("你得到了 #b#t" + itemId + "##k " + quantity + "个。");
            } else {
                cm.sendOk("你确实有#b#t4110010##k吗?如果是,请你确认在背包的装备,消耗,其他窗口中是否有一格以上的空间?");
            }
            cm.safeDispose();
        } else {
            cm.sendOk("今天的运气可真差，什么都没有拿到。");
            cm.safeDispose();
        }
    } else {
        cm.dispose();
    }
}